<hr/><a name="_details"></a><h2>Detailed Description</h2>
<p>The Three20 network request object, built to work with the Three20 cache and co. </p>
<p>Allows customization of the http method, body, and parameters, as well as natural response processing using <a class="el" href="protocol_t_t_u_r_l_response-p.php" title="A response protocol for TTURLRequest.">TTURLResponse</a> objects. </p>
