<hr/><a name="_details"></a><h2>Detailed Description</h2>
<p>A view controller that displays an action sheet. </p>
<p>This class exists in order to allow action sheets to be displayed by <a class="el" href="interface_t_t_navigator.php" title="A URL-based navigation system with built-in persistence.">TTNavigator</a>, and gain all the benefits of persistence and URL dispatch.</p>
<p>By default this controller is not persisted in the navigation history. </p>
